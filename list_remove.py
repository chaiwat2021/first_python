# normal remove
thislist = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(thislist)

# remove with in index
thislist = ["apple", "banana", "cherry"]
thislist.pop(1)
print(thislist)

# remove from the end of the list
thislist = ["apple", "banana", "cherry"]
thislist.pop()
print(thislist)

# remove with del command
thislist = ["apple", "banana", "cherry"]
del thislist[0]
print(thislist)

# delete entire list
thislist = ["apple", "banana", "cherry"]
del thislist

# clear list
thislist = ["apple", "banana", "cherry"]
thislist.clear()
print(thislist)

